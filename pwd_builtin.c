#include "main.h"
/* ************************************************************************** */
#define PWD_NO_ERROR	0
#define PWD_ERROR		-1
#define CWD_LEN			4096
/* ************************************************************************** */
int pwd_builtin(void)
{
	char	cwd_name[CWD_LEN];
	
	if (getcwd(cwd_name, CWD_LEN) == NULL)	
		return (PWD_ERROR);
	else
	{
		write(1, cwd_name, strlen(cwd_name));
		write(1, "\n", 1);
	}
	
	return (PWD_NO_ERROR);
}

